    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        Personal pers = new Personal();
        pers.setNome(this.getInsertp_nome().getText());
        pers.setCpf(this.getInsertp_cpf().getText());
        pers.setEspecialidade(this.getInsertp_especialidade().getText());
        pers.setEmail(this.getInsertp_email().getText());
        pers.setSenha(this.getInsertp_senha().getText());
        pers.setDataNasc(this.getInsertp_datanasc().getText());
        pers.setSalario(1500);
        pers.setBonus_por_aluno(0);
        pers.setQtd_aluno(0);
        pers.setTipo("personal");
        
        Cadastro cadp = new Cadastro();
        try {
            cadp.inserir(pers);
            JOptionPane.showMessageDialog(this, "Cadastro realizado com sucesso!");
            this.setVisible(false);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Erro ao cadastrar: " + ex.getMessage());
            JOptionPane.showMessageDialog(this, "Tente novamente!");
            Logger.getLogger(Tela_cadastro_aluno.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        this.setVisible(false);
        
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

